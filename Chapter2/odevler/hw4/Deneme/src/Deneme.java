public class Deneme {

    public static void main(String[] args) {
        int x = 7, y = 5;
        System.out.println("7 + 5 = " + (x + y));
        System.out.println("7 - 5 = " + (x - y));
        System.out.println("7 * 5 = " + (x * y));
        System.out.println("7 / 5 = " + ((double) x / y));
        System.out.println("7 % 5 = " + (x % y));
    }
}
